package oca.cap3.arreglos;

/**
 * Declaración de arreglos con tipos primitivos
 * @author Chris
 */
public class _1ArregloPrimitivos {

	// forma más común
	int[] numeros1 = new int[3];
	
	// declaración e inicialización de los elementos (redundante)
	int[] numeros2 = new int[] {10, 22, 54};
	
	// simplifica la anterior (arreglo anónimo)
	int[] numeros3 = {10, 22, 54};
	
	// La posición de los corchetes es arbitraria
	// el espacio antes y después de los corchetes es opcional
	int[] numeroAnimales1;
	int [] numeroAnimales2;
	int numeroAnimales3[];
	int numeroAnimales4 [];
	
	// Declaraciones múltiples en una línea
	// Declara dos arreglos
	int[] ids1, tipos1;
	// Declara un arreglo y un primitivo (int)
	int ids2[], tipos2;
}
